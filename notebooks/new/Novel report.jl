macro newtake_repeatedly!(expr)
	quote
		let
			channel = $(esc(expr))
			_update, set_update = @use_state(take!(channel))
			@use_task([channel]) do
				inner_channel = channel
				for update in inner_channel
					set_update(update)
				end
			end
			_update
		end
	end
end

